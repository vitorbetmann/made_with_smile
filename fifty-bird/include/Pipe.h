#ifndef PIPE_H
#define PIPE_H

// —————————————————————————————————————————————————————————————————————————————————————————————————
// Includes
// —————————————————————————————————————————————————————————————————————————————————————————————————

#include <raylib.h>

// —————————————————————————————————————————————————————————————————————————————————————————————————
// Data types
// —————————————————————————————————————————————————————————————————————————————————————————————————

typedef enum
{
    TOP,
    BOTTOM,
} Orientation;

typedef struct
{
    Orientation orientation;
    float x, y;
    int width, height;
    Texture sprite;
} Pipe;

// —————————————————————————————————————————————————————————————————————————————————————————————————
// Prototypes
// —————————————————————————————————————————————————————————————————————————————————————————————————

Pipe *pipeInit(float y, Orientation o);
void pipeUpdate(Pipe *p, float dt);
void pipeDraw(const Pipe *p);
void pipeUnload(Pipe *p);

#endif
